// Route to get schedule for a specific group
app.get('/schedule/:group', (req, res) => {
    const group = req.params.group;
    if (scheduleData[group]) {
        res.json(scheduleData[group]);
    } else {
        res.status(404).json({ error: 'Group not found' });
    }
});

// Route to update schedule
app.post('/schedule', (req, res) => {
    const { group, day, time, subject } = req.body;
    const cellId = `${day}-${time}`;
    if (scheduleData[group] && scheduleData[group][cellId]) {
        scheduleData[group][cellId] = subject;
        res.json({ message: 'Schedule updated successfully' });
    } else {
        res.status(404).json({ error: 'Group or cell not found' });
    }
});

